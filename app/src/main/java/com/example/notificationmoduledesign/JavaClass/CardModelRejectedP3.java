package com.example.notificationmoduledesign.JavaClass;

public class CardModelRejectedP3 {
    String nameRU, numberRU, dateRU, timeRU;

    public CardModelRejectedP3( String nameRU, String numberRU, String dateRU, String timeRU) {

        this.nameRU = nameRU;
        this.numberRU = numberRU;

        this.dateRU = dateRU;
        this.timeRU = timeRU;
    }


    public String getNameRU() {
        return nameRU;
    }

    public String getNumberRU() {
        return numberRU;
    }





    public String getDateRU() {
        return dateRU;
    }

    public String getTimeRU() {
        return timeRU;
    }
}